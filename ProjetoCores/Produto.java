public class Produto{
    String nome;
    double valor;
    String descricao;
    int qntEstoque;
    
    public Produto(String nome, double valor, String descricao, int qntEstoque){
        this.nome=nome;
        this.valor=valor;
        this.descricao=descricao;
        this.qntEstoque=qntEstoque;
    }
}